<script setup>
import gsap from 'gsap';
let ctx;
let shapes;
let letters;
let main = ref();
const end_xpos = [0, 18, 35, 50];
const end_dirs = [-1, 1, 1, -1];

onMounted(() => {
  ctx = gsap.context((self) => {
    let el;
    shapes = gsap.utils.toArray('.logo-half');
    const letter = self.selector('path');
    letters = gsap.utils.toArray(letter);
    const w = 18;

    // set up for loader animation
    shapes.forEach((shape, i) => {
      gsap.set(shape, { x: i * (w + 2) });

      gsap.fromTo(
        shape,
        { scaleY: 0.5 },
        {
          duration: 0.3,
          delay: (shapes.length - i) * -0.05,
          scaleY: 1.5,
          ease: 'quad.inOut',
          repeat: -1,
          yoyo: true,
        }
      );
    });
  }, main.value);

  setTimeout(function () {
    removeLoader();
  }, 1200);
});

onUnmounted(() => {
  ctx.revert();
});

const removeLoader = () => {
  // move shapes into logo lockup
  shapes.forEach((shape, i) => {
    gsap.to(shape, {
      overwrite: 'auto',
      duration: 0.5,
      delay: i * 0.1,
      x: end_xpos[i],
      scaleY: 1,
      scaleX: end_dirs[i],
      ease: 'back.out(3)',
    });
  });

  // show logo text
  shuffle(letters);
  gsap.fromTo(
    letters,
    { y: 20, opacity: 0 },
    {
      duration: 0.75,
      opacity: 1,
      y: 0,
      stagger: 0.03,
      ease: 'back.out(2)',
    }
  );
};
</script>

